#include<stdio.h>

int main(){
    for(int i = 1 ; i <= 20 ; i ++){
        printf("%d ",i);
    }
    return 0;
}

//explanatiion
// i = 1 {initialization}
// i <= 20 the loop stops when i is not > or = to 20;
// i++ i increases by one for every iteration
// print i (every single iteration , i is printed )